class Order:
	def getId(self): 
		return self.Id;
	
	def setId(self, Id): 
		self.Id = Id;
	
	def getIsPay(self): 
		return self.isPay;
	
	def setIsPay(self, isPay): 
		self.isPay = isPay;
	
	def getOrderTime(self): 
		return self.orderTime;
	
	def setOrderTime(self, orderTime): 
		self.orderTime = orderTime;
	
	def getPersonNum(self): 
		return self.personNum;
	
	def setPersonNum(self, personNum): 
		self.personNum = personNum;
	
	def getRemark(self): 
		return self.remark;
	
	def setRemark(self, remark): 
		self.remark = remark;
	
	def getTableId(self): 
		return self.tableId;
	
	def setTableId(self, tableId): 
		self.tableId = tableId;
	
	def getUserId(self): 
		return self.userId;
	
	def setUserId(self, userId): 
		self.userId = userId;
