class QueryOrderDetail:

	def getName(self):
		return self.name
	
	def setName(self, name):
		self.name = name
	
	def getNum(self):
		return self.num
	
	def setNum(self, num):
		self.num = num
	
	def getPrice(self):
		return self.price
	
	def setPrice(self, price):
		self.price = price
	
	def getRemark(self):
		return self.remark
	
	def setRemark(self, remark):
		self.remark = remark
	
	def getTotal(self):
		return self.total
	
	def setTotal(self, total):
		self.total = total
	
	

