class Menu:
	def getId(self):
		return self.Id

	def setId(self, Id): 
		self.Id = Id

	def getName(self): 
		return self.name

	def setName(self, name): 
		self.name = name

	def getPic(self): 
		return self.pic

	def setPic(self, pic): 
		self.pic = pic

	def getPrice(self): 
		return self.price

	def setPrice(self, price): 
		self.price = price

	def getRemark(self): 
		return self.remark

	def setRemark(self, remark): 
		self.remark = remark

	def getTypeId(self): 
		return self.typeId

	def setTypeId(self, typeId): 
		self.typeId = typeId
