class QueryOrder:
	def getName(self):
		return self.name
	

	def setName(self, name):
		self.name = name
	

	def getOrderTime(self):
		return self.orderTime
	

	def setOrderTime(self, orderTime):
		self.orderTime = orderTime
	

	def getPersonNum(self):
		return self.personNum
	

	def setPersonNum(self, personNum):
		self.personNum = personNum
	

	def getTableId(self):
		return self.tableId
	

	def setTableId(self, tableId):
		self.tableId = tableId
