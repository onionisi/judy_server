class Table:
	def getId(self):
		return self.Id

	def setId(self, Id): 
		self.Id = Id

	def getNum(self): 
		return self.num

	def setNum(self, num): 
		self.num = num

	def getFlag(self): 
		return self.flag

	def setFlag(self, flag): 
		self.flag = flag

	def getDesc(self): 
		return self.desc

	def setDesc(self, desc): 
		self.desc = desc
