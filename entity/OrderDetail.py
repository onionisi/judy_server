class OrderDetail:
	def getId(self):
		return self.Id
	
	def setId(self, Id):
		self.Id = Id
	
	def getMenuId(self):
		return self.menuId
	
	def setMenuId(self, menuId):
		self.menuId = menuId
	
	def getNum(self):
		return self.num
	
	def setNum(self, num):
		self.num = num
	
	def getOrderId(self):
		return self.orderId
	
	def setOrderId(self, orderId):
		self.orderId = orderId
	
	def getRemark(self):
		return self.remark
	
	def setRemark(self, remark):
		self.remark = remark
